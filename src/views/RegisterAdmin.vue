<template>
  <div class="container">
    <div class="row register-page">
      <form class="col s12" id="reg-form">
        <div class="row">
          <div class="errorMsg">
            {{ errorLastName }}
          </div>
          <div class="input-field col s6">
            <input
              id="last_name"
              type="text"
              class="validate"
              v-model="lastName"
              required
            />
            <label for="last_name">姓</label>
          </div>
          <div class="errorMsg">
            {{ errorFirstName }}
          </div>
          <div class="input-field col s6">
            <input
              id="first_name"
              type="text"
              class="validate"
              v-model="firstName"
              required
            />
            <label for="first_name">名</label>
          </div>
        </div>
        <div class="row">
          <div class="errorMsg">
            {{ errorZipcode }}
          </div>
          <div class="input-field col s6">
            <input
              id="zipcode"
              type="text"
              class="validate"
              v-model="zipcode"
              required
            />
            <br />
            <label for="zipcode">郵便番号</label>
          </div>
          <button type="button" @click="showAddress">検索</button>
        </div>

        <div class="row">
          <div class="errorMsg">
            {{ errorAddress }}
          </div>
          <div class="input-field col s12">
            <input
              id="address"
              type="text"
              class="validate"
              v-model="address"
              required
            />
            <label for="address">住所</label>
          </div>
        </div>
        <div class="row">
          <div class="errorMsg">
            {{ errorMailAddress }}
          </div>
          <div class="input-field col s12">
            <input
              id="email"
              type="email"
              class="validate"
              v-model="mailAddress"
              required
            />
            <label for="email">メールアドレス</label>
          </div>
        </div>
        <div class="row">
          <div class="errorMsg">
            {{ errorPassword }}
          </div>
          <div class="input-field col s12">
            <input
              id="password"
              type="password"
              class="validate"
              minlength="8"
              v-model="password"
              required
            />
            <label for="password">パスワード</label>
          </div>
          <div class="errorMsg">
            {{ passCheckErr }}
          </div>
          <div class="input-field col s12">
            <input
              id="checkpassword"
              type="password"
              class="validate"
              minlength="8"
              v-model="checkpassword"
              required
            />
            <label for="checkpassword">確認用パスワード</label>
          </div>
        </div>
        <div class="row">
          {{ displayMsg }}
          <div class="input-field col s6">
            <button
              class="btn btn-large btn-register waves-effect waves-light"
              type="button"
              v-on:click="registerAdmin"
            >
              登録
              <i class="material-icons right">done</i>
            </button>
          </div>
        </div>
      </form>
    </div>
  </div>
</template>

<script lang="ts">
import { Component, Vue } from "vue-property-decorator";
import config from "@/const/const";
import axios from "axios";
/**
 * 管理者登録をする画面.
 */
@Component
export default class RegisterAdmin extends Vue {
  // 姓
  private lastName = "";
  // 名
  private firstName = "";
  // 郵便番号
  private zipcode = "";
  // 住所
  private address = "";
  // メールアドレス
  private mailAddress = "";
  // パスワード
  private password = "";
  private checkpassword = "";
  // エラー
  private passCheckErr = "";
  //登録エラー
  private displayMsg = "";

  // エラー
  private errorLastName = "";
  private errorFirstName = "";
  private errorZipcode = "";
  private errorAddress = "";
  private errorMailAddress = "";
  private errorPassword = "";
  private errorFlag = false;

  /**
   * 管理者情報を登録する.
   *
   * @remarks
   * 本メソッドは非同期でWebAPIを呼び出し管理者登録をするためasync/await axiosを利用しています。これらを利用する場合は明示的に戻り値にPromiseオブジェクト型を指定する必要があります。
   * @returns Promiseオブジェクト
   */
  async registerAdmin(): Promise<void> {
    // パスワードチェック
    if (this.password !== this.checkpassword) {
      this.passCheckErr = "パスワードがまちがっています。";
      this.errorFlag = true;
    }
    // エラー表示
    if (this.lastName === "") {
      this.errorLastName = "姓を入力してください";
      this.errorFlag = true;
    }
    if (this.firstName === "") {
      this.errorFirstName = "名を入力してください";
      this.errorFlag = true;
    }
    if (this.mailAddress === "") {
      this.errorMailAddress = "メールアドレスを入力してください";
      this.errorFlag = true;
    }
    if (this.password === "") {
      this.errorPassword = "パスワードを入力してください";
      this.errorFlag = true;
    }
    if (this.checkpassword === "") {
      this.passCheckErr = "パスワードを入力してください";
      this.errorFlag = true;
    }
    if (this.zipcode === "") {
      this.errorZipcode = "郵便番号を入力してください";
      this.errorFlag = true;
    }
    if (this.errorAddress === "") {
      this.errorAddress = "住所を入力してください";
      this.errorFlag = true;
    }
    // 処理の中断
    if (this.errorFlag === true) {
      return;
    }

    // 管理者登録処理
    const response = await axios.post(`${config.EMP_WEBAPI_URL}/insert`, {
      name: this.lastName + " " + this.firstName,
      mailAddress: this.mailAddress,
      password: this.password,
    });
    console.dir("response:" + JSON.stringify(response));

    // エラーチェック処理
    const errorCheck = JSON.stringify(response);
    if (errorCheck.includes("error")) {
      this.displayMsg = "登録できませんでした";
      return;
    }

    this.$router.push("/loginAdmin");
  }

  async showAddress(): Promise<void> {
    // エラーを消す
    // eslint-disable-next-line @typescript-eslint/no-var-requires
    const axiosJsonpAdapter = require("axios-jsonp");

    console.log(this.zipcode);
    const response = await axios.get(`https://zipcoda.net/api`, {
      adapter: axiosJsonpAdapter,
      params: {
        zipcode: this.zipcode,
      },
    });

    console.dir("response" + JSON.stringify(response));
    console.log(response);

    this.address = response.data.items[0].address;
  }
}
</script>

